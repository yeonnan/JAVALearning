package Etc;

public class Type1 {

    /**
     *
     * int, double, boolean, char, string으로 변수 선언해보기
     * int는 내가 좋아하는 숫자
     * double은 원주율
     * boolean은 민트 호, 불호
     * char 자기 성 이니셜 첫 글자
     * string은 자기 한국어 이름
     */
    public static void main(String[] args) {
        int myFavoriteNumber = 9;
        double pi = 3.141592;
        boolean likeMint = false;
        char myFirstName = 'A';
        String myname = "안녕";

        System.out.println(myFavoriteNumber);
        System.out.println(pi);
        System.out.println(likeMint);
        System.out.println(myFirstName);
        System.out.println(myname);

    }
}
